    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // TODO add your handling code here:
        //Si hi ha algun check activat, es procedeix a l'actualització
        boolean isAnyChk = chkMan.isSelected() || chkCat.isSelected() || chkCustomer.isSelected() || chkSupplier.isSelected() || chkProd.isSelected() || chkOrder.isSelected() || chkDescuentos.isSelected();
        if (isAnyChk) {
            Instant start = null;
            //Crear connexió i procedir segons checks:
            //Thread t = new MyThread(jProgressBar1);
            boolean parametres = true;
            if (chkOrder.isSelected()) {
                parametres = checkParametres();
            }
            GestorTraspas gt =null;
            if (!parametres) {
                gt = null;
            }
            else {
                //t.start();
                start = Instant.now();
                this.setEnabled(false);
                gt = establirConnexio();
//                gt = new GestorTraspas(1);
            }
            if (gt!=null) {
                //realitzar tasques segons chk
                
                if (chkMan.isSelected()) {
                    try {
                        gt.actualitzarMarques();
                    } catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (chkCat.isSelected()) {
                    try {
                        gt.actualitzarFamilies();
                    } catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (chkCustomer.isSelected()) {
                    try {
                        gt.actualitzarClients();
                    } catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (chkSupplier.isSelected()) {
                    try {
                        gt.actualitzarProveidors();
                    } catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (chkProd.isSelected()) {
                    try {
                        gt.actualitzarArticlesFiltrant(fpCodDesde.getText(), fpCodHas.getText(), fpFamDesde.getText(), fpFamHas.getText(), fpManDesde.getText(), fpManHas.getText(), txtAlmacen.getText());
                    }
                    catch(Exception ex) {
                        System.out.println(ex.getMessage() + ex.getCause());
                    }
                }
                if (chkOrder.isSelected()) {
                    String vendedor = txtVenDef.getText().trim();
                    try {
                        gt.actualitzarPedidos(vendedor, idListaPrecio, idDepartament, idEmpresa, idEmpleat, idRepresentant);
                    }
                    catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                if (chkDescuentos.isSelected()) {
                    try {
                        if (!chkMan.isSelected()) {
                            gt.actualitzarMarques();
                        }
                        if (!chkCat.isSelected()) {
                            gt.actualitzarFamilies();
                        }
                        gt.actualitzarDescomptes();
                    } catch (GestioTraspasException ex) {
                        Logger.getLogger(SaiConnectorUI.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                
                //Tancar la connexió:
                gt.close();
                Instant end = Instant.now();
                
                
                if (start!=null) {
                    Duration timeElapsed = Duration.between(start, end);
                    System.out.println("Time taken: "+ timeElapsed.toMillis() +" milliseconds");
                    txtTime.setText("TIME: "  + timeElapsed);
                    //t.interrupt();
                }
                //finalitzar:
                fiActualitzacio();
                this.setEnabled(true);
                //Guardar les ip's en l'arxiu de configuració
                guardarIps();
            }
            else {
                //fer avís!
                //dialogConnection.show();
                this.setEnabled(true);
                dialogConnection.setVisible(true);
                this.setEnabled(false);
                //t.interrupt();
            }
            //Si la connexió té èxit, guardarem en l'arxiu de configuració les dades de la pestanya config
        }
        else {
            this.setEnabled(true);
            //pop-up usuari, no has seleccionat cap opcio!
            System.out.println("FUNCIONAAAAAA");
            //dialogOpcions.show();
            dialogOpcions.setVisible(true);
            //this.disable(); deprecated, use 'setEnabled'
            this.setEnabled(false);
        }
        
            
        
    }//GEN-LAST:event_btnActualizarActionPerformed

