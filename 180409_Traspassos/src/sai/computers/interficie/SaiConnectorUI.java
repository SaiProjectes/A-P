    private void guardarIps() {
            Properties prop = new Properties();
            OutputStream output = null;
            try {
                output = new FileOutputStream("config.properties");

                prop.setProperty("sqlserverip",txtIpSql.getText());
                prop.setProperty("sqlserverdb",txtBdSql.getText());
                prop.setProperty("sqlserverusr",txtUsrSql.getText());
                prop.setProperty("sqlserverpwd",String.valueOf(txtPwdSql.getPassword()));

                prop.setProperty("mysqlip",txtIpMy.getText());
                prop.setProperty("mysqldb",txtBdMy.getText());
                prop.setProperty("mysqlusr",txtUsrMy.getText());
                prop.setProperty("mysqlpwd",String.valueOf(txtPwdMy.getPassword()));

                prop.setProperty("rutaimg",txtRutaPresta.getText());
                prop.setProperty("vendedor",txtVenDef.getText());
                prop.setProperty("imgdef", txtRutaImgDef.getText());
                
                prop.setProperty("almahora", txtAlmacen.getText());
                
                prop.setProperty("idlistaprecio", idListaPrecio);
                
                prop.setProperty("idDepartamento", idDepartament);
                prop.setProperty("idEmpresa", idEmpresa);
                prop.setProperty("idEmpleado", idEmpleat);
                prop.setProperty("idRepresentante", idRepresentant);
                
               
                
                
                
                // save properties to project root folder
                prop.store(output, null);

            } catch (IOException io) {
                io.printStackTrace();
            } finally {
                if (output != null) {
                    try {
                        output.close();
                    } catch (IOException e) {
                        e.printStackTrace();
                    }
                }

            }
    }
    
    
    
